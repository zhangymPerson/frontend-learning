/*
  ============================================================
  文件：ts/types.ts
  作用：定义项目中用到的所有 TypeScript 类型和接口
  ============================================================

  【知识点 1】为什么要单独放类型定义？
  - 类型是 TypeScript 的核心概念
  - 集中管理类型，让代码更清晰、更好维护
  - 其他 .ts 文件可以从这里 import 类型

  【知识点 2】interface vs type
  - interface：定义"对象的形状"（有哪些属性、什么类型）
  - type：可以定义任何类型（对象、联合类型、元组等）
  - 两者大多数情况下可以互换
  - 推荐：定义对象用 interface，其他场景用 type
  ============================================================ */


/* ============================================================
   【知识点 3】interface - 接口（最核心的 TS 特性之一）

   interface 用来描述一个对象"长什么样"：
   - 有哪些属性
   - 每个属性是什么类型
   - 哪些属性是必须的，哪些是可选的

   用中文说：interface 就是一份"对象的合同"
   ============================================================ */

/**
 * 项目信息接口
 * 描述项目基本信息的数据结构
 */
export interface ProjectInfo {
  /** 项目名称 */
  name: string;

  /** 项目版本号 */
  version: string;

  /** 作者 */
  author: string;

  /** 包含的页面名称列表 */
  pages: string[];

  /** 是否已发布 */
  isPublished: boolean;
}

/**
 * 表单数据接口
 * 描述联系表单提交的数据结构
 */
export interface ContactFormData {
  /** 用户姓名 */
  name: string;

  /** 邮箱地址 */
  email: string;

  /** 消息内容 */
  message: string;
}

/**
 * 表单验证结果接口
 * 描述表单验证的结果
 */
export interface ValidationResult {
  /** 是否验证通过 */
  isValid: boolean;

  /** 验证失败时的错误信息（验证通过时为空字符串） */
  errorMessage: string;
}


/* ============================================================
   【知识点 4】type - 类型别名

   type 可以给一个类型起个新名字，也可以定义复杂类型：
   ============================================================ */

/**
 * 颜色值类型
 * 只能是这 4 个字符串之一
 *
 * 【知识点 5】联合类型（Union Type）
 * - 用 | 分隔，表示"可以是类型A 或 类型B 或 ..."
 * - "red" | "blue" 表示只能是 "red" 或 "blue"
 * - 这种限制值范围的写法叫"字面量类型"
 */
export type ThemeColor = '#667eea' | '#764ba2' | '#f093fb' | '#f5576c';

/**
 * 消息类型
 * 区分成功和错误的消息
 */
export type MessageType = 'success' | 'error';

/**
 * 问候消息接口
 * 描述一条问候语的数据结构
 */
export interface GreetingMessage {
  /** 消息文本 */
  text: string;

  /** 消息类型 */
  type: MessageType;
}


/* ============================================================
   【知识点 6】枚举（Enum）

   enum 定义一组命名常量，比直接用字符串或数字更清晰：
   ============================================================ */

/**
 * 页面路径枚举
 * 集中管理所有页面的路径，避免到处写字符串
 *
 * 【知识点 7】为什么用枚举？
 * - 如果到处写 "index.html"，拼写错了不容易发现
 * - 用枚举后，写错会直接报编译错误！
 * - 修改路径时只需改枚举定义一处
 */
export enum PagePath {
  /** 首页 */
  Home = 'index.html',

  /** 关于页面 */
  About = 'pages/about.html',

  /** 联系页面 */
  Contact = 'pages/contact.html',
}
